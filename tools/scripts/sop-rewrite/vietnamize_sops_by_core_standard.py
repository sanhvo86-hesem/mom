def compile_patterns(rel_path: str) -> tuple[list[tuple[re.Pattern[str], str]], list[tuple[re.Pattern[str], str]]]:
    early_items = list(EARLY_COMMON_REPLACEMENTS)
    early_items.extend(FILE_REPLACEMENTS.get(rel_path, []))
    late_items = list(LATE_COMMON_REPLACEMENTS)
    return (
        [(re.compile(pattern, re.IGNORECASE), replacement) for pattern, replacement in early_items],
        [(re.compile(pattern, re.IGNORECASE), replacement) for pattern, replacement in late_items],
    )


def protect_terms(text: str) -> tuple[str, dict[str, str]]:
    protected: dict[str, str] = {}
    counter = 0

    keep_patterns = [
        r"\b(?:[A-Z]{1,8}-\d{3}|IG\d+|G\d+|JD-[A-Z0-9]+|D-[A-Z0-9]+)\b",
        r"\b(?:RecordType|StatusCode|JobNum|CustomerID|EvidenceUrl|StatusText|RecordID|PartNum|LotNum)\b",
    ]
    for token in sorted(KEEP_TERMS, key=len, reverse=True):
        keep_patterns.append(rf"(?<![A-Za-z0-9-]){re.escape(token)}(?![A-Za-z0-9-])")

    combined = re.compile("|".join(f"(?:{pattern})" for pattern in keep_patterns))

    def replacer(match: re.Match[str]) -> str:
        nonlocal counter
        key = f"__KEEP_{counter}__"
        protected[key] = match.group(0)
        counter += 1
        return key

    return combined.sub(replacer, text), protected


def translate_text_node(
    text: str,
    early_patterns: list[tuple[re.Pattern[str], str]],
    late_patterns: list[tuple[re.Pattern[str], str]],
) -> str:
    result = text
    for pattern, replacement in early_patterns:
        result = pattern.sub(replacement, result)
    protected_text, protected = protect_terms(result)
    result = protected_text
    for pattern, replacement in late_patterns:
        result = pattern.sub(replacement, result)
    for key, value in protected.items():
        result = result.replace(key, value)
    return result


def translate_html(
    html_text: str,
    early_patterns: list[tuple[re.Pattern[str], str]],
    late_patterns: list[tuple[re.Pattern[str], str]],
) -> tuple[str, bool]:
    protected_blocks: dict[str, str] = {}
    block_index = 0

    def protect_block(match: re.Match[str]) -> str:
        nonlocal block_index
        key = f"__BLOCK_{block_index}__"
        protected_blocks[key] = match.group(0)
        block_index += 1
        return key

    working = SCRIPT_STYLE_RE.sub(protect_block, html_text)
    working = SECTION3_BLOCK_RE.sub(protect_block, working)
    parts = TEXT_SPLIT_RE.split(working)
    changed = False

    for index, part in enumerate(parts):
        if index % 2 == 1 or not part.strip():
            continue
        translated = translate_text_node(part, early_patterns, late_patterns)
        if translated != part:
            parts[index] = translated
            changed = True

    result = "".join(parts)
    for key, block in protected_blocks.items():
        result = result.replace(key, block)
    return result, changed


def iter_targets(args: list[str]) -> list[Path]:
    if not args:
        return sorted(SOP_ROOT.rglob("*.html"))

    targets: list[Path] = []
    for raw in args:
        candidate = Path(raw)
        if not candidate.is_absolute():
            candidate = ROOT / raw
        if candidate.is_dir():
            targets.extend(sorted(candidate.rglob("*.html")))
        elif candidate.is_file():
            targets.append(candidate)
    return targets


def main() -> None:
    changed_files = 0
    for path in iter_targets(sys.argv[1:]):
        rel_path = path.relative_to(SOP_ROOT).as_posix()
        early_patterns, late_patterns = compile_patterns(rel_path)
        if not early_patterns and not late_patterns:
            continue
        original = path.read_text(encoding="utf-8")
        normalized = ftfy.fix_text(original) if ftfy is not None else original
        translated, changed = translate_html(normalized, early_patterns, late_patterns)
        changed = changed or translated != original
        if not changed:
            continue
        path.write_text(translated, encoding="utf-8", newline="")
        changed_files += 1
        print(f"UPDATED {path.relative_to(ROOT).as_posix()}")
    print(f"UPDATED_FILES={changed_files}")


if __name__ == "__main__":
    main()
